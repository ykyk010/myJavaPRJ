        int nuber1 = 10,  nuber2 = 20;
        int resAdd;
        int resSub;
        int resMul;
        int resDiv;

        resAdd = nuber1 + nuber2;
        resSub = nuber1 - nuber2;
        resMul = nuber1 * nuber2;
