package Ykyk010;

public class Y05 {
    public static void main(String[] args) {
        int rA, rB, rC, rD, rE;
        int a = 30;
        int b = 20;

        rA = a + b;
        rB = a - b;
        rC = a * b;
        rD = a / b;
        rE = a % b;

        System.out.println(a + "+" + b + "=" + rA);
        System.out.println(a + "-" + b + "=" + rB);
        System.out.println(a + "*" + b + "=" + rC);
        System.out.println(a + "/" + b + "=" + rD);
        System.out.println(a + "%" + b + "=" + rE);
    }
}
